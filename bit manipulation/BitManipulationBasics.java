public class BitManipulationBasics {
    public static void main(String[] args) {
        int a = 5;
        int b = 6;
        System.out.println(a & b);
        System.out.println(a | b);
        System.out.println(a ^ b);
        System.out.println(a << 2);
        System.out.println(a >> 2);
        System.out.println(~a);
        System.out.println(~b);
    }
}