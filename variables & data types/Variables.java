public class Variables {
    public static void main(String[] args) {
        int a = 10;
        int b = 20;
        System.out.println(a);
        System.out.println(b);
        String text = "hello world";
        System.out.println(text);
        a = 30;
        b = a;
        System.out.println(a);
        System.out.println(b);
    }
}